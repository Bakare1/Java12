package JCL;

import java.util.OptionalLong;

public class OptionalLongDemo {
    public static void main(String[] args) {
        // Create a OptionalLong instance
       // using of() method
        OptionalLong opLong= OptionalLong.of(21438999);
       // Get value of this OptionalLong instance
        System.out.println("OptionalLong: "+ opLong);
    }
}
